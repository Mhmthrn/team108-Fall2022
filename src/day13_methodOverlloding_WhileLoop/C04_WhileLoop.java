package day13_methodOverlloding_WhileLoop;

public class C04_WhileLoop {
    public static void main(String[] args) {

        /*
        2 basmakli sayilardan 7 ile bolunebilen leri yazdirin
         */
        for (int i =10; i <100 ; i++) {
            if (i%7==0){
                System.out.print(i+" ");
            }

            }
        System.out.println("");
        System.out.println("_+_+_+_+_+_+_+_+_+_+_+_+_+_+_");

        int sayi=10;
        while (sayi<100){
            if (sayi%7==0){
                System.out.print(sayi+" ");
            }
            sayi++;
        }

        /*
        forLoop ile yailabilecek bir soruyu illa da while loop ile yapmak biraz zahmetli

         */







        }

    }


